def get_translations(lang: str) -> dict:
    """Return translation dict for the given language. Falls back to English."""
    return LANGUAGES.get(lang, LANGUAGES["en"])


def get_language_label(lang: str) -> str:
    """Return the display label for a language."""
    labels = {"zh": "中文", "en": "EN"}
    return labels.get(lang, "EN")


def get_alternate_lang(lang: str) -> str:
    """Return the alternate language code."""
    return "en" if lang == "zh" else "zh"
